// Shifting and Operator overloading
#include<iostream>
#include<conio.h>
using namespace std;
int main()
{
 int x,y;
 cout<<"\n Enter any number for Left shift: ";
 cin>>x;
 cout<<"\n Enter Other number for Right shift: ";
 cin>>y;
 cout<<"\n After Shifting Left  : "<<(x<<2);
 cout<<"\n After Shifting Right : "<<(y>>2);
 return 0;
 }
